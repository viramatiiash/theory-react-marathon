var arguments = [1, 2, 3];
var arr = () => arguments[0];
console.log(arr()); // 1. Тут стрілочна функція використовую зовнішній масив arguments, а не свій об'єкт arguments.

function foo(n) {
  var f = () => arguments[0] + n;
  return f();
}

console.log(foo(3)); // 6

function foo1(n) {
  var f = () => arguments[1] + n;
  return f();
}

console.log(foo1(3, 4)); // 7
// ! Тобто, у стрілочної функції немає свого об'єкта arguments, а у звичайної функції є.
